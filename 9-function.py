#فانکشن 
#برای شاخت فانکشن از کلمه کلیدی def استفاده میکنیم بعد برای فانکشن یه سام و جلوی اسم پرانتز میزاریم

#مثال

def sajjad():
    print("sajadghadimi")
#sajjad == اسم فانکشنتون
sajjad()

#خروجی: sajadghadimi
#توی فانکشن ها هر کاری دلت بخواد میتونی بکنی از حلقه ها گزفته تا شرط ها

#ما به فانکشن ها ورودی هم میتونیم بدیم مثال

def show(name): #همیشه سعی کن ورودی معنی دار باشه مثلا ننویس xیا a
    print("hello " + name)

show("sajjad")

#خروجی : hello sajjad
 
#show == اسم فانکشنتون


#و حتی پیشرتف تر هم میشه  کرد 
